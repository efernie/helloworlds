/**
 * Java Variables
 * ( I'am a multi line comment )
 */

// I am another comment style

public class HelloVariables {

  public static void main(String[] args) {

    /**
     * Boolean
     * True/false
     */
    boolean zed = true;

    /**
     * Byte
     * 8 bit
     */
    byte alpha = 10;

    /**
     * Char
     * 16 bit unicode character
     */
    char charlie = 'C';

    /**
     * Double
     */
    double daisy = 123.4;

    /**
     * Float
     */
    float frank = 123.4f;

    /**
     * integer / int
     * 32 bit
     */
    int bar = 1;

    /**
     * String
     *
     */
    String foo = "I be a string";


    // Print out the varibles
    System.out.println("Boolean: " + zed);
    System.out.println("Byte: " + alpha);
    System.out.println("Char: " + charlie);
    System.out.println("Double: " + daisy);
    System.out.println("Float: " + frank);
    System.out.println("Integer: " + bar);
    System.out.println("String: " + foo);

  }

}
